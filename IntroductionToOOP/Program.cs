using System;

namespace Solution
{
  public class SolutionClass
  {
    public static void Main(string[] arg)
    {
      int number = Convert.ToInt32(Console.ReadLine());
      
      string answer;
      
      if (number % 2 == 0)
        {
          answer = "Even";
        }
      
      else
        {
          answer = "Odd";
        }
          }
  }
}
